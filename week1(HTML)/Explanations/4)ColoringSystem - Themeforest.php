Coloring System:
    Attribute:
        1)style:
            -we use style attribute to change style/decoration of any element
            Properties:
                1)color:
                    -to change text color:
                    Example:
                        <h3 style="color: red;">Ahmed</h3>
                        -change color to red
                2)background-color :
                    -to change background color of the element
                    -we should put semicolon(;) between properties
                    Example:
                        <h3 style="color: red; background-color: gray;">Ahmed</h3>

    -Coloring system in Web:
        -coloring system in web is R G B
        -R(red), G(green), B(blue)
        -any color consists of percentages of these 3primary color(RGB) 
        -R G B , each color is represented by 2 digits(hexa decimal)
                R   G   B
                00  00  00  min (BLACK)
                FF  FF  FF  max (white)
                FF  00  00  (red)
                00  FF  00  (green)
                00  00  FF  (blue)

        -HexaDecimal: 0123456789ABCDEF

        Example:
            color:#0198cc
            
        -sometimes we put only 3 digits:
            -if we have color: 00 99 cc
            -then we can remove the repeated digit in each color : 09c
            Example:
                color : #09c;
